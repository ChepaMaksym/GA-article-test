function cases = synthetic_reproduction_cases(kind)
if nargin < 1 || isempty(kind)
    kind = 'reproduction';
end

article = article_reported();
L = article.pipeline_length_m;
nominalA = 1120;
nominalV = 1.20;

positionsKm = [18, 31, 47, 63, 79, 95, 112, 128, 145, 158];
trueA = [1120, 1114, 1131, 1098, 1162, 1045, 1127, 1103, 1148, 1010];
flowMultiplier = [1.00, 0.60, 0.75, 0.90, 1.05, 1.20, 1.15, 0.70, 1.10, 0.85];
syncOffset = [0.00, 0.20, -0.35, 0.70, -1.10, 1.50, -1.80, 2.00, -0.90, 1.20];
timingNoise = [0.000, 0.006, -0.004, 0.012, -0.018, 0.020, -0.024, 0.030, -0.015, 0.018];
weights = [1.0, 1.4, 1.2, 1.5, 1.8, 2.0, 2.0, 2.2, 1.7, 1.9];
classes = {'baseline', 'low_flow', 'low_flow', 'sync_error', 'sync_error', ...
    'high_flow', 'sync_error', 'max_sync_error', 'wave_bias', 'wave_bias'};

cases = repmat(empty_case(), 1, numel(positionsKm));
for i = 1:numel(positionsKm)
    cases(i) = build_case( ...
        sprintf('case_%02d_%s', i, classes{i}), ...
        L, ...
        positionsKm(i) * 1000, ...
        trueA(i), ...
        nominalV * flowMultiplier(i), ...
        syncOffset(i), ...
        timingNoise(i), ...
        nominalA, ...
        nominalV, ...
        weights(i), ...
        classes{i});
end

if strcmpi(kind, 'unit')
    cases = cases(3);
elseif strcmpi(kind, 'sandbox')
    cases = add_sandbox_cases(cases, L, nominalA, nominalV);
elseif ~strcmpi(kind, 'reproduction')
    error('Unknown synthetic case kind: %s', kind);
end
end

function c = build_case(name, L, trueX, trueA, trueV, syncOffset, timingNoise, nominalA, nominalV, weight, scenarioClass)
c = empty_case();
c.data_type = 'synthetic_reproduction';
c.name = name;
c.scenario_class = scenarioClass;
c.pipeline_length_m = L;
c.true_leak_position_m = trueX;
c.true_wave_speed_mps = trueA;
c.true_velocity_mps = trueV;
c.true_sync_offset_s = syncOffset;
c.timing_noise_s = timingNoise;
c.nominal_wave_speed_mps = nominalA;
c.nominal_velocity_mps = nominalV;
c.nominal_sync_offset_s = 0;
c.measured_wave_speed_mps = trueA + deterministic_wave_calibration_error(trueA, nominalA);
c.measured_velocity_mps = trueV + deterministic_velocity_calibration_error(trueV, nominalV);
c.measured_sync_offset_s = syncOffset + deterministic_sync_calibration_error(syncOffset);
c.weight = weight;
c.note = 'Synthetic case for GA-NPW workflow verification; not author field data.';
end

function cases = add_sandbox_cases(cases, L, nominalA, nominalV)
extra = [ ...
    build_case('sandbox_wave_minus_10_midline', L, 87000, nominalA * 0.90, nominalV, 0.00, 0.010, nominalA, nominalV, 2.5, 'wave_minus_10'); ...
    build_case('sandbox_wave_plus_10_far', L, 153000, nominalA * 1.10, nominalV * 0.95, 0.40, -0.012, nominalA, nominalV, 2.5, 'wave_plus_10'); ...
    build_case('sandbox_high_noise_low_flow', L, 42000, nominalA * 0.96, nominalV * 0.60, -1.20, 0.050, nominalA, nominalV, 2.8, 'high_noise_low_flow'); ...
    build_case('sandbox_high_noise_high_flow', L, 132000, nominalA * 1.04, nominalV * 1.20, 1.60, -0.045, nominalA, nominalV, 2.8, 'high_noise_high_flow'); ...
    build_case('sandbox_sync_minus_2', L, 67000, nominalA, nominalV * 0.88, -2.00, 0.015, nominalA, nominalV, 3.0, 'sync_minus_2'); ...
    build_case('sandbox_sync_plus_2', L, 119000, nominalA, nominalV * 1.08, 2.00, -0.015, nominalA, nominalV, 3.0, 'sync_plus_2')];
cases = [cases, extra']; %#ok<AGROW>
end

function err = deterministic_wave_calibration_error(trueA, nominalA)
err = 0.18 * (nominalA - trueA);
err = max(min(err, 18), -18);
end

function err = deterministic_velocity_calibration_error(trueV, nominalV)
err = 0.20 * (nominalV - trueV);
err = max(min(err, 0.08), -0.08);
end

function err = deterministic_sync_calibration_error(syncOffset)
err = -0.12 * syncOffset;
err = max(min(err, 0.18), -0.18);
end

function c = empty_case()
c = struct( ...
    'data_type', '', ...
    'name', '', ...
    'scenario_class', '', ...
    'pipeline_length_m', NaN, ...
    'true_leak_position_m', NaN, ...
    'true_wave_speed_mps', NaN, ...
    'true_velocity_mps', NaN, ...
    'true_sync_offset_s', NaN, ...
    'timing_noise_s', NaN, ...
    'nominal_wave_speed_mps', NaN, ...
    'nominal_velocity_mps', NaN, ...
    'nominal_sync_offset_s', NaN, ...
    'measured_wave_speed_mps', NaN, ...
    'measured_velocity_mps', NaN, ...
    'measured_sync_offset_s', NaN, ...
    'weight', NaN, ...
    'note', '');
end
